namespace BindOpen.Commands
{
    /// <summary>
    /// This enumeration lists all the possible option name kinds.
    /// </summary>
    public enum OptionNameKind
    {
        /// <summary>
        /// Any.
        /// </summary>
        Any,

        /// <summary>
        /// Only name.
        /// </summary>
        OnlyName,

        /// <summary>
        /// Name with value.
        /// </summary>
        NameWithValue,

        /// <summary>
        /// Name then value.
        /// </summary>
        NameThenValue,

        /// <summary>
        /// Only value.
        /// </summary>
        OnlyValue,
    }
}